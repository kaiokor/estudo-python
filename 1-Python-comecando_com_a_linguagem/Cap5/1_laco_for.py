# FUNÇÃO RANGE

for rodada in range (1,10,2):
    print(rodada)

# range(start, stop, [step])

# rodada é valor atual ao decorrer do range definido
# range é o tamanho q o "rodada" vai percorrer
# (1,10,2)
# 1 é o valor de início do range
# 10 é o valor que quando o "rodada" chegar, vai sair do for, por isso que ele não aparece na contagem
# 2 é o valor de quanto em quanto que rodada vai percorer o rage, ou seja, se 2 em 2
